class Dog
  # #name= method to set the instance variable @name
  def name=(name)
    @name = name
  end
  # #name method to get @name
  def name
    @name
  end
  
  # #breed= method to set the variable @breed
  def breed=(breed)
    @breed = breed
  end
  # #breed method to get @breed
  def breed
    @breed
  end
end